export const Marquee: React.FC = () => {
  const containerRef = useRef<HTMLDivElement>(null);

  useEffect(() => {
    const speed = 2.5;
    const loop = horizontalLoop(".marquee-inner", {
      repeat: -1,
      speed: 1.5,
      paddingRight: 0,
    });

    let tl: any;

    Observer?.create({
      target: window,
      type: "wheel",
      onChangeY: (self) => {
        tl && tl.kill();
        const factor = self.deltaY > 0 ? -1 : 1;
        tl = gsap
          .timeline()
          .to(loop, { timeScale: speed * factor, duration: 0.25 })
          .to(loop, { timeScale: 1 * factor, duration: 1 });
      },
    });

    return () => {
      tl?.kill();
    };
  }, []);

  return (
    <div className="overflow-x-hidden w-full flex items-center">
      <div
        ref={containerRef}
        style={{
          boxShadow: "0 0 50px 0 rgba(255,54, 0, 0.3)",
        }}
        className="flex flex-row items-center w-[100%] overflow-hidden relative"
      >
        <MarqueeRow index={0} />
        <MarqueeRow index={1} />
      </div>
    </div>
  );
};
